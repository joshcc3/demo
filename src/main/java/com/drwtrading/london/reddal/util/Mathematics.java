    public static double toQuantityFromNotionalInSafetyCurrency(final double notionalInSafetyCurrency, final long price,
            final IBook<?> book, final double wpv) {

        if (0 == price) {
            return 0;
        } else {
            final double priceEUR = toEUR(book.getCCY(), price / Constants.NORMALISING_FACTOR);
            final double notionalValue = wpv * priceEUR;
            return notionalInSafetyCurrency / notionalValue;
        }
    }

    public static double getPointValue(final InstType instType, final String symbol, final ITickTable tickTable) {

        switch (instType) {
            case FUTURE: {
                final FutureConstant future = FutureConstant.getFutureFromSymbol(symbol);
                if (null != future) {
                    return future.wholePointValue;
                } else {
                    final Double tickValue = TICK_VALUES.get(symbol.substring(0, symbol.length() - 2));
                    if (null != tickValue) {
                        final long normalizedTickIncrement = tickTable.getRawTickLevels().firstKey();
                        return tickValue / ((double) normalizedTickIncrement / Constants.NORMALISING_FACTOR);
                    } else {
                        throw new IllegalStateException("No tick value found for futures market:" + symbol);
                    }
                }
            }
            default: {
                return 1d;
            }
        }
    }

    private static double toEUR(final CCY fromCCY, final long price) {

        return TO_EUR_RATES.get(fromCCY) * price;
    }

