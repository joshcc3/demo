    public static final long NORMALIZING_FACTOR = PriceFormat.ONE_NORMALIZED_UNIT;

    private static final ConcurrentHashMap<Currency, Double> exchangeRates = new ConcurrentHashMap<>();
    private static final ConcurrentHashMap<String, Double> tickValues = new ConcurrentHashMap<>();
    private static Currency safetyCurrency = Currency.EUR;

    static {
        initializeExchangeRates();
        initializeTickValues();
    }


    public static boolean hasCurrency(Currency currency) {
        return exchangeRates.containsKey(currency);
    }

    public static boolean hasTickValue(String market) {
        return tickValues.containsKey(market);
    }

    public static boolean isSafetyCurrency(Currency currency) {
        return currency == safetyCurrency;
    }

    public static boolean aIsGreaterThanB(BigDecimal a, BigDecimal b) {
        return a.compareTo(b) > 0;
    }


    public static boolean aIsGreaterThanB(final double doubleA, final double doubleB) {
        return doubleA > doubleB;
    }

    public static Currency getSafetyCurrency() {
        return safetyCurrency;
    }

    public static BigDecimal d2bd(double d) {
        return BigDecimal.valueOf(d);
    }

    public static ImmutableMap<Currency, Double> getRates() {
        return ImmutableMap.copyOf(exchangeRates);
    }

    // Notional conversions

    public static double toNotional(final long orderPrice, int quantity, final InstrumentDefinitionEvent refData) {
        return quantity * getNotionalPrice(orderPrice, refData);
    }

    public static double toQuantityFromNotional(final double notional, final long price, final InstrumentDefinitionEvent refData) {
        if (price == 0) {
            return 0.0;
        }
        return notional / getNotionalPrice(price, refData);
    }

    public static double toNotionalInSafetyCurrency(final long orderPrice, int quantityDelta, final InstrumentDefinitionEvent refData) {
        return toNotional(orderPrice, quantityDelta, refData) * rate(refData.getPriceStructure().getCurrency());
    }

    public static double toQuantityFromNotionalInSafetyCurrency(final double notionalInSafetyCurrency, final long price, final InstrumentDefinitionEvent refData) {
        if (price == 0) {
            return 0;
        }
        return toQuantityFromNotional(notionalInSafetyCurrency, price, refData) / rate(refData.getPriceStructure().getCurrency());
    }

    // Private utilities and initialization

    private static double getNotionalPrice(final long price, final InstrumentDefinitionEvent refData) {
        final double rawPrice = ((double) price / NORMALIZING_FACTOR);
        final double pointValue = getPointValue(refData);
        return pointValue * rawPrice;
    }

    private static double getPointValue(final InstrumentDefinitionEvent refData) {
        // Get point value for futures
        double pointValue = 1.0;
        if (refData.getInstrumentStructure() instanceof FutureOutrightStructure) {
            long normalizedTickIncrement = refData.getPriceStructure().getTickIncrement();
            Double tickValue = tickValues.get(refData.getMarket());
            if (tickValue != null) {
                pointValue = tickValue / ((double) normalizedTickIncrement / NORMALIZING_FACTOR);
            } else {
                throw new IllegalStateException("No tick value found for futures market:" + refData.getMarket());
            }
        }
        return pointValue;
    }

    private static Double rate(final Currency currency) {
        if (currency == safetyCurrency) {
            return 1.0;
        }
        return exchangeRates.get(currency);
    }

    public static long floorMod(long a, long b) {
        return (a % b + b) % b;
    }

    public static int floorMod(int a, int b) {
        return (a % b + b) % b;
    }

